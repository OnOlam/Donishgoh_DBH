// ============ МОДАЛ ОЙНАНИ ОЧИШ ============
function openNewsModal(newsId) {
    const data = newsData[newsId];
    if (!data) {
        console.error('Хабар топилмади:', newsId);
        return;
    }
    
    const modal = document.getElementById('newsModal');
    const modalTitle = document.getElementById('newsModalTitle');
    const modalDate = document.getElementById('newsModalDate');
    const modalText = document.getElementById('newsModalText');
    
    if (!modal || !modalTitle || !modalDate || !modalText) {
        console.error('Модал элементлари топилмади');
        return;
    }
    
    modalTitle.innerHTML = data.title;
    modalDate.innerHTML = data.date;
    modalText.innerHTML = data.fullText;
    
    modal.style.display = 'flex';
    document.body.style.overflow = 'hidden';
}

// ============ МОДАЛ ОЙНАНИ ЁПИШ ============
function closeNewsModal() {
    const modal = document.getElementById('newsModal');
    if (modal) {
        modal.style.display = 'none';
        document.body.style.overflow = 'auto';
    }
}

// ============ ТУГМАЛАРГА ВОҚЕА БИРИКТИРИШ ============
document.addEventListener('DOMContentLoaded', function() {
    // Ҳамаи "Батафсил" тугмаларини топиб, уларга клик бўйича воқеа боғлаш
    const readMoreBtns = document.querySelectorAll('.read-more, [data-news-id]');
    
    readMoreBtns.forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            
            const newsId = this.getAttribute('data-news-id');
            if (newsId && newsData[newsId]) {
                openNewsModal(newsId);
            } else {
                console.error('Хато: news-id топилмади ёки маълумот мавҷуд эмас', newsId);
            }
        });
    });
    
    // Модални ойнадан ташқари босса ёпиш
    const modal = document.getElementById('newsModal');
    if (modal) {
        window.addEventListener('click', function(e) {
            if (e.target === modal) {
                closeNewsModal();
            }
        });
    }
});